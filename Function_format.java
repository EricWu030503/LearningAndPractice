public class Function_format {
    public static void main(String[] args){
        System.out.println(triple(3));
    }
    public static int triple(int x){
        return x*3;
    }
}
/*
function format:
access(public/private) + static/non-static + return type + function name + (type parameters){
    function body
}
if no access modifier(public/private), then the method is package-private in default
 */